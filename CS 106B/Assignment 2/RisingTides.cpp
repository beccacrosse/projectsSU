/*
 * This code maps out possible flooding locations on a grid
 * using flood sources and water height and comparing them to
 * a grid of the topography of an area.
 */


/*points to a later function which checks the areas surrounding
 * each floodpoint to see if they are floodable and in bounds */
Vector<GridLocation> CheckSurrounding(const Grid<double>& terrain,
                                      const GridLocation& location);

/*  takes as input a terrain (given as a Grid<double>), a list of locations of water sources (represented
as a Vector<GridLocation>; more on GridLocation later), and the height of the water level, then returns a
Grid<bool> indicating, for each spot in the terrain, whether it’s under water (true) or above the water (false).
*/
Grid<bool> floodedRegionsIn(const Grid<double>& terrain,
                            const Vector<GridLocation>& sources,
                            double height) {
    /*  takes as input a terrain (given as a Grid<double>), a list of locations of water sources (represented
    as a Vector<GridLocation>; more on GridLocation later), and the height of the water level, then returns a
    Grid<bool> indicating, for each spot in the terrain, whether it’s under water (true) or above the water (false).
    */
    Grid<bool> floodBool (terrain.numRows(), terrain.numCols(), false);
    Queue<GridLocation> floodQueue;
    for (GridLocation location: sources) {
        if(terrain.inBounds(location) &&
                height >= terrain[location]){
            floodBool[location] = true;
            floodQueue.enqueue(location);
            }
        }
    while (!floodQueue.isEmpty()) {
        GridLocation currentLocation = floodQueue.dequeue();
        Vector<GridLocation> surroundings = CheckSurrounding(terrain, currentLocation);
        for (GridLocation direction: surroundings) {
            if (height >= terrain[direction] && !floodBool[direction]){
                floodBool[direction] = true;
                floodQueue.enqueue(direction);
            }
        }
    }
    return floodBool;
}

/* This helper function takes in a Grid<double> of a terrain and
 * a Gridlocation or a location and returns a vector of the Gridlocations
 * in each cardinal direction of the GridLocation if they are in range of
 * the terrain*/
Vector<GridLocation> CheckSurrounding(const Grid<double>& terrain, const GridLocation& location){
    Vector<GridLocation> directions;
    GridLocation up = location;
    up.row++;
    GridLocation down = location;
    down.row--;
    GridLocation left = location;
    left.col--;
    GridLocation right = location;
    right.col++;

    directions += up;
    directions += down;
    directions += left;
    directions += right;
    Vector<GridLocation> validDirections;
    for(GridLocation current: directions){
        if(terrain.inBounds(current)){
            validDirections.add(current);
        }
    }
    return validDirections;
}


